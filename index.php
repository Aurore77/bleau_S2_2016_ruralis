            <div class="edit">
                <h5>Un constat</h5>
                <p>Quand on parle des problématiques de l’aménagement des territoires ruraux, nous observons que les grands médias (l’audiovisuel et  la presse écrite) ne semblent s’y intéresser que de façon  épisodique et fragmentée au seul  ressort des éruptions sociales, des colères économiques ou de la détresse culturelle.</p>
            </div>
            <div class="edit">
                <h5>qui conforte</h5>
                <p>Ce constat nous conduit à croire qu’une nouvelle aventure éditoriale est possible.  Nous nous inscrivons dans un journalisme de profondeur et de solutions.  Cette démarche journalistique promet de décrypter les enjeux à venir et de diffuser l’envie d’agir.
                </p>
            </div>
            <div class="edit">
                <h5>notre projet éditorial</h5>
                <p>RURALIS Magazine, un trimestriel de 96 pages en quadrichromie au format 19x27.  Un travail d’enquête approfondie de terrain, de rencontres, d’échanges, de questionnement au service de l’évolution des campagnes françaises.</p>
            </div>
        </section>

